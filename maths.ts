
// Math module implements some basic function

/**
 * add a and b
 * @param a 
 * @param b 
 * @returns 
 */

function add(a:number,b:number):number{
    return a + b + 10;

}


function subtract(a:number,b:number):number{
    return a-b;
}

/**
 * if a and b are equal, return true, otherwise return false
 * @param a add a number a
 * @param b add a number b
 * @returns 
 */
function boolean(a:number,b:number):boolean{
    return a===b;
}


function multiply(a:number,b:number):number{
    return a*b+100;
}
